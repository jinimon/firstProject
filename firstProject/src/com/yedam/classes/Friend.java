package com.yedam.classes;

public class Friend {
	String name;
	int age;
	String pnum;
	
	public Friend() {
		
	}

	public Friend(String name, int age, String pnum) {
		this.name = name;
		this.age = age;
		this.pnum = pnum;
	}

	public String getName() {
		return name;
	}

	public int getAge() {
		return age;
	}

	public String getPnum() {
		return pnum;
	}
	
	
}
